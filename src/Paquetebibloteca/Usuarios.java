    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        // Registrar los datos
        String nombre = jTextFieldNombre.getText().trim();
        String apellido = jTextFieldApellido.getText().trim();
        String cedula = jTextFieldCedula.getText().trim();
        String nacionalidad = jTextFieldNacionalidad.getText().trim();
        String telefono = jTextFieldTelefono.getText().trim();
        String correo = jTextFieldCorreo.getText().trim();
        String direccion = jTextFieldDireccion.getText().trim();
        
        JOptionPane.showMessageDialog(null,"Datos Guardados");
        
           try {
            DataOutputStream dos = new DataOutputStream(new FileOutputStream(Usuario, true));
            DataInputStream dis = new DataInputStream(new FileInputStream(Usuario));
            intro(dos, nombre,apellido,cedula,nacionalidad,telefono,correo,direccion );

        } catch (Exception e) {
            e.getStackTrace();
        }
        
    }//GEN-LAST:event_BtnGuardarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // Boton para agregar datos
        DefaultTableModel modelo=(DefaultTableModel) Tabla.getModel();
         Object [] fila=new Object[7]; 
        fila[0]=jTextFieldNombre.getText(); 
        fila[1]=jTextFieldApellido.getText(); 
        fila[2]=jTextFieldCedula.getText(); 
        fila[3]=jTextFieldNacionalidad.getText(); 
        fila[4]=jTextFieldTelefono.getText(); 
        fila[5]=jTextFieldDireccion.getText();
        fila[6]=jTextFieldCorreo.getText();
        
          modelo.addRow(fila); 
           Tabla.setModel(modelo); 
           
         JOptionPane.showMessageDialog(null,"Datos Guardados");
    }//GEN-LAST:event_btnRegistrarActionPerformed

