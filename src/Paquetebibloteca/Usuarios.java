public static void intro(DataOutputStream dos, String nombre,String apellido, String cedula, String nacionalidad, String telefono, String correo, String direccion) throws IOException {
        dos.writeUTF(nombre + "\n");
        dos.writeUTF(apellido + "\n");
        dos.writeUTF(cedula + "\n");
        dos.writeUTF(nacionalidad + "\n");
        dos.writeUTF(telefono + "\n");
        dos.writeUTF(correo + "\n");
        dos.writeUTF(direccion + "\n");

    }
